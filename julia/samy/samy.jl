include("tiling.jl")
include("plot.jl")

function samy()
	t = Tiling()
	p(x) = add_point!(t, x)
	l(x, y) = link!(t, x, y)
	ngon(x, y, n) = add_regular_polygon!(t, x, y, n)

	ngon(Point(0, 0), Point(1, 0), 3)
	ngon(1,3,3)
	ngon(1,4,3)
	ngon(1,5,3)
	ngon(1,6,3)
	ngon(1,7,3)
	ngon(3,2,3)
	ngon(8,2,4)
	ngon(2,7,4)
	ngon(7,6,4)
	ngon(11,7,3)
	ngon(3,8,3)
	ngon(4,3,6)
	ngon(9,11,3)
	ngon(11,13,12)
	ngon(9, 18, 3)
	ngon(18, 27, 4)
	ngon(20,19,12)
	ngon(27,26, 6)
	ngon(30,12,3)
	ngon(30, 6, 4)
	ngon(31,30,3)
	ngon(9,28, 3)
	ngon(42,5,4)
	ngon(43,4,3)
	ngon(28,29,4)
	ngon(29,41,3)
	ngon(10,28,3)
	ngon(38,21, 3)
	ngon(45,41,6)
	ngon(41,40,3)
	ngon(26,25,4)
	ngon(25,24,6)
	ngon(24,23,4)
	ngon(23,22,6)
	ngon(22,21,4)
	ngon(38,37,4)

	return t
end

t = samy()

fig = Figure(size = (1000, 1000))
ax = Axis(fig[1, 1], aspect = DataAspect(), title = "Samy", xlabel = "X", ylabel = "Y")

plot(t, ax, :red, show_points = true, text = true)

display(fig)
